-- Talent pool draw system for Fog Dungeon Ranking.
-- Safe to run after player_profiles_migration.sql / second_beta_reset.sql.

begin;

create table if not exists public.talent_pool_items (
  pool_key text not null,
  talent_id integer not null,
  talent_name text not null,
  rank text not null check (rank in ('B', 'C')),
  created_at timestamptz not null default now(),
  primary key (pool_key, talent_id)
);

create table if not exists public.talent_draw_state (
  invite_code_hash text primary key check (char_length(invite_code_hash) = 64),
  spent_draws integer not null default 0 check (spent_draws >= 0),
  updated_at timestamptz not null default now()
);

create table if not exists public.talent_pool_counters (
  invite_code_hash text not null check (char_length(invite_code_hash) = 64),
  pool_key text not null,
  continue_draw integer not null default 0 check (continue_draw >= 0),
  updated_at timestamptz not null default now(),
  primary key (invite_code_hash, pool_key)
);

create table if not exists public.owned_talents (
  id bigint generated by default as identity primary key,
  invite_code_hash text not null check (char_length(invite_code_hash) = 64),
  pool_key text not null,
  talent_id integer not null,
  talent_name text not null,
  rank text not null check (rank in ('B', 'C')),
  acquired_from text not null default 'draw' check (acquired_from in ('draw', 'exchange')),
  acquired_at timestamptz not null default now(),
  unique (invite_code_hash, pool_key, talent_id)
);

create table if not exists public.user_fragments (
  invite_code_hash text primary key check (char_length(invite_code_hash) = 64),
  fragment_total integer not null default 0 check (fragment_total >= 0),
  updated_at timestamptz not null default now()
);

create table if not exists public.talent_draw_logs (
  id bigint generated by default as identity primary key,
  invite_code_hash text not null check (char_length(invite_code_hash) = 64),
  pool_key text not null,
  draw_type text not null check (draw_type in ('single', 'ten')),
  talent_id integer not null,
  talent_name text not null,
  rank text not null check (rank in ('B', 'C')),
  is_guarantee boolean not null default false,
  is_repeat boolean not null default false,
  fragment_gain integer not null default 0 check (fragment_gain >= 0),
  draw_time timestamptz not null default now()
);

create table if not exists public.talent_exchange_logs (
  id bigint generated by default as identity primary key,
  invite_code_hash text not null check (char_length(invite_code_hash) = 64),
  pool_key text not null,
  target_talent_id integer not null,
  target_talent_name text not null,
  cost_fragment integer not null check (cost_fragment > 0),
  exchange_time timestamptz not null default now()
);

create index if not exists talent_pool_items_pool_idx on public.talent_pool_items(pool_key, rank, talent_id);
create index if not exists owned_talents_user_idx on public.owned_talents(invite_code_hash, acquired_at desc);
create index if not exists talent_draw_logs_user_idx on public.talent_draw_logs(invite_code_hash, draw_time desc);
create index if not exists talent_exchange_logs_user_idx on public.talent_exchange_logs(invite_code_hash, exchange_time desc);

