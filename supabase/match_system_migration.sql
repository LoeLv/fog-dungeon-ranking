grant select, insert, update, delete on public.match_queue to service_role;
grant select, insert, update, delete on public.match_room_players to service_role;
grant execute on function public.join_match_queue(uuid, text, text) to service_role;
grant execute on function public.cancel_match_queue(uuid, text) to service_role;
