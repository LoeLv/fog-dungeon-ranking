-- Fragment rollback:
-- wrong draws may have granted fragments, so subtract those;
-- wrong exchanges spent fragments, so refund those.
with trickery_profiles as (
  select invite_code_hash from public.player_profiles where original_faith_god = '欺诈'
), allowed_pools as (
  select invite_code_hash, 'Pool欺诈' as pool_key from trickery_profiles
  union all
  select p.invite_code_hash,
    case p.profession
      when '杂技演员' then 'Pool战士'
      when '诡术大师' then 'Pool法师'
      when '小丑' then 'Pool牧师'
      when '驭兽师' then 'Pool猎人'
      when '受害者' then 'Pool刺客'
      when '魔术师' then 'Pool歌者'
      else null
    end
  from public.player_profiles p
  where p.original_faith_god = '欺诈'
), wrong_draws as (
  select l.invite_code_hash, count(*) as draw_count, coalesce(sum(l.fragment_gain), 0) as remove_fragment
  from public.talent_draw_logs l
  join trickery_profiles p on p.invite_code_hash = l.invite_code_hash
  where not exists (select 1 from allowed_pools a where a.invite_code_hash = l.invite_code_hash and a.pool_key = l.pool_key)
  group by l.invite_code_hash
), wrong_exchanges as (
  select e.invite_code_hash, coalesce(sum(e.cost_fragment), 0) as refund_fragment
  from public.talent_exchange_logs e
  join trickery_profiles p on p.invite_code_hash = e.invite_code_hash
  where not exists (select 1 from allowed_pools a where a.invite_code_hash = e.invite_code_hash and a.pool_key = e.pool_key)
  group by e.invite_code_hash
), fragment_fix as (
  select
    coalesce(d.invite_code_hash, e.invite_code_hash) as invite_code_hash,
    coalesce(d.remove_fragment, 0) as remove_fragment,
    coalesce(e.refund_fragment, 0) as refund_fragment
  from wrong_draws d
  full join wrong_exchanges e on e.invite_code_hash = d.invite_code_hash
)
update public.user_fragments f
set fragment_total = greatest(0, f.fragment_total - fragment_fix.remove_fragment + fragment_fix.refund_fragment),
    updated_at = now()
from fragment_fix
where f.invite_code_hash = fragment_fix.invite_code_hash;

with trickery_profiles as (
