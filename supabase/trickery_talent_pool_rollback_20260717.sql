      when '受害者' then 'Pool刺客'
      when '魔术师' then 'Pool歌者'
      else null
    end
  from public.player_profiles p
  where p.original_faith_god = '欺诈'
), wrong_draws as (
  select l.invite_code_hash, count(*) as draw_count
  from public.talent_draw_logs l
  join trickery_profiles p on p.invite_code_hash = l.invite_code_hash
