-- Roll back talents caused by the old trickery display bug.
--
-- Rule after the fix:
-- - If original_faith_god = '欺诈', the real faith pool is always Pool欺诈.
-- - A profession pool is allowed only when the currently stored profession is still a 欺诈 profession.
-- - Talents/logs/counters from other pools are considered bug output and can be removed.
--
-- Run section 1 first. Run section 2 only after the preview is correct.

-- 1) Preview wrong-pool records.
with trickery_profiles as (
  select
    invite_code_hash,
    display_name,
    faith_god,
    original_faith_god,
    profession,
    case profession
      when '杂技演员' then 'Pool战士'
      when '诡术大师' then 'Pool法师'
      when '小丑' then 'Pool牧师'
      when '驭兽师' then 'Pool猎人'
      when '受害者' then 'Pool刺客'
      when '魔术师' then 'Pool歌者'
      else null
    end as trickery_profession_pool
  from public.player_profiles
  where original_faith_god = '欺诈'
), allowed_pools as (
  select invite_code_hash, 'Pool欺诈' as pool_key from trickery_profiles
  union all
  select invite_code_hash, trickery_profession_pool
  from trickery_profiles
  where trickery_profession_pool is not null
)
select bucket, display_name, pool_key, rows, fragment_delta
from (
  select 'owned_talents' as bucket, p.display_name, o.pool_key, count(*) as rows, 0::numeric as fragment_delta
  from public.owned_talents o
  join trickery_profiles p on p.invite_code_hash = o.invite_code_hash
  where not exists (
    select 1 from allowed_pools a where a.invite_code_hash = o.invite_code_hash and a.pool_key = o.pool_key
  )
  group by p.display_name, o.pool_key
  union all
  select 'talent_overflow_choices', p.display_name, c.pool_key, count(*), 0::numeric
  from public.talent_overflow_choices c
  join trickery_profiles p on p.invite_code_hash = c.invite_code_hash
  where not exists (
    select 1 from allowed_pools a where a.invite_code_hash = c.invite_code_hash and a.pool_key = c.pool_key
  )
  group by p.display_name, c.pool_key
  union all
  select 'talent_draw_logs', p.display_name, l.pool_key, count(*), coalesce(sum(l.fragment_gain), 0)
  from public.talent_draw_logs l
  join trickery_profiles p on p.invite_code_hash = l.invite_code_hash
  where not exists (
    select 1 from allowed_pools a where a.invite_code_hash = l.invite_code_hash and a.pool_key = l.pool_key
  )
  group by p.display_name, l.pool_key
  union all
  select 'talent_exchange_logs', p.display_name, e.pool_key, count(*), coalesce(sum(e.cost_fragment), 0)
  from public.talent_exchange_logs e
  join trickery_profiles p on p.invite_code_hash = e.invite_code_hash
  where not exists (
    select 1 from allowed_pools a where a.invite_code_hash = e.invite_code_hash and a.pool_key = e.pool_key
  )
  group by p.display_name, e.pool_key
  union all
  select 'talent_pool_counters', p.display_name, c.pool_key, count(*), 0::numeric
  from public.talent_pool_counters c
  join trickery_profiles p on p.invite_code_hash = c.invite_code_hash
  where not exists (
    select 1 from allowed_pools a where a.invite_code_hash = c.invite_code_hash and a.pool_key = c.pool_key
  )
  group by p.display_name, c.pool_key
) audit
order by display_name, bucket, pool_key;

